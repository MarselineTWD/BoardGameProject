export function buildGenerateCampaignPrompt(theme) {
  return GENERATE_CAMPAIGN_PROMPT.replace('{{THEME_INPUT}}', theme);
}

export function buildGenerateLobbyScenarioPrompt({ game, theme }) {
  return GENERATE_LOBBY_SCENARIO_PROMPT.replaceAll('{{GAME_TITLE}}', game.title)
    .replaceAll('{{GAME_DESCRIPTION}}', game.description)
    .replaceAll('{{GAME_ID}}', game.id)
    .replaceAll('{{THEME_INPUT}}', theme);
}

export function buildGenerateCharacterPrompt({
  game,
  scenarioSummary,
  existingCharacters,
}) {
  return GENERATE_CHARACTER_PROMPT.replaceAll('{{GAME_TITLE}}', game.title)
    .replaceAll('{{SCENARIO_SUMMARY}}', scenarioSummary)
    .replaceAll('{{EXISTING_CHARACTERS}}', existingCharacters);
}

export function wrapPlayerMessage({
  characterName,
  characterId,
  currentActorId,
  playerMessage,
}) {
  return PLAYER_MESSAGE_WRAPPER.replace('{{CHARACTER_NAME}}', characterName)
    .replace('{{CHARACTER_ID}}', characterId)
    .replace('{{CURRENT_ACTOR_ID}}', currentActorId ?? 'none')
    .replace('{{PLAYER_MESSAGE}}', playerMessage);
}
