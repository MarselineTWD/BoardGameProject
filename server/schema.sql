CREATE TABLE IF NOT EXISTS games (
  slug TEXT PRIMARY KEY,
  name TEXT NOT NULL,
  year_published INTEGER,
  bgg_id INTEGER UNIQUE,
  bgg_rating NUMERIC(4, 2),
  bgg_rank INTEGER,
  bgg_weight NUMERIC(4, 2),
  bgg_num_ratings INTEGER NOT NULL DEFAULT 0,
  min_players INTEGER NOT NULL DEFAULT 1,
  max_players INTEGER NOT NULL DEFAULT 1,
  best_player_counts INTEGER[] NOT NULL DEFAULT '{}',
  playing_time_min INTEGER,
  playing_time_max INTEGER,
  min_age INTEGER,
  dna_strategy NUMERIC(5, 2),
  dna_luck NUMERIC(5, 2),
  dna_interaction NUMERIC(5, 2),
  dna_complexity NUMERIC(5, 2),
  dna_length NUMERIC(5, 2),
  dna_scaling NUMERIC(5, 2),
  dna_replayability NUMERIC(5, 2),
  dna_accessibility NUMERIC(5, 2),
  wikidata_id TEXT NOT NULL DEFAULT '',
  thumbnail_url TEXT NOT NULL DEFAULT '',
  image_url TEXT NOT NULL DEFAULT '',
  is_featured BOOLEAN NOT NULL DEFAULT false,
  is_expansion BOOLEAN NOT NULL DEFAULT false,
  description TEXT NOT NULL DEFAULT '',
  meta_description TEXT NOT NULL DEFAULT '',
  source TEXT NOT NULL DEFAULT 'postgres'
);

CREATE INDEX IF NOT EXISTS games_featured_idx ON games (is_featured, is_expansion);
CREATE INDEX IF NOT EXISTS games_rank_idx ON games (bgg_rank);
CREATE INDEX IF NOT EXISTS games_name_idx ON games (lower(name));

